"""generate_provenance.py — derive daemon/eval/CORPUS_PROVENANCE.md directly
from the corpus JSONL files' existing `source`/`notes` fields.

Generated, not hand-maintained: re-run this after any `build_corpus.py`
change so the provenance document never drifts from the actual corpus.

Usage
-----
    python daemon/eval/generate_provenance.py
"""
from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path

_THIS_DIR = Path(__file__).parent
CORPUS_DIR = _THIS_DIR / "corpus"
OUTPUT = _THIS_DIR / "CORPUS_PROVENANCE.md"
ALL_CORPORA = ("malicious", "typosquat", "hallucinated", "benign")


def _load(name: str) -> list[dict]:
    path = CORPUS_DIR / f"{name}.jsonl"
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def main() -> None:
    metadata_path = CORPUS_DIR / "corpus_metadata.json"
    metadata = json.loads(metadata_path.read_text(encoding="utf-8")) if metadata_path.exists() else {}
    version = metadata.get("corpus_version", "unknown")

    lines: list[str] = [
        "# CIDAS Evaluation Corpus — Provenance",
        "",
        f"Generated from `daemon/eval/corpus/*.jsonl` by `generate_provenance.py`. "
        f"Corpus version: **{version}**. Do not edit this file by hand — re-run the "
        f"generator after any `build_corpus.py` change.",
        "",
        f"Generated at: {datetime.now(timezone.utc).isoformat()}",
        "",
    ]

    for corpus_name in ALL_CORPORA:
        records = _load(corpus_name)
        lines.append(f"## {corpus_name.capitalize()} ({len(records)} records)")
        lines.append("")
        if corpus_name in ("malicious",):
            lines.append("| Package | Version | Attack Type | Source | Notes |")
            lines.append("|---|---|---|---|---|")
            for r in records:
                lines.append(
                    f"| `{r['package_name']}` | `{r['version']}` | {r.get('attack_type', '')} "
                    f"| {r.get('source', '')} | {r.get('notes', '')} |"
                )
        elif corpus_name == "typosquat":
            lines.append("| Package | Target | Mutation | npm_registered | Notes |")
            lines.append("|---|---|---|---|---|")
            for r in records:
                lines.append(
                    f"| `{r['package_name']}` | `{r.get('target', '')}` | {r.get('mutation', '')} "
                    f"| {r.get('npm_registered', '')} | {r.get('notes', '')} |"
                )
        elif corpus_name == "hallucinated":
            lines.append("| Package | Notes |")
            lines.append("|---|---|")
            for r in records:
                lines.append(f"| `{r['package_name']}` | {r.get('notes', '')} |")
        else:  # benign
            lines.append(f"Source: `{records[0].get('source', 'n/a') if records else 'n/a'}` "
                         f"— {records[0].get('notes', '') if records else ''}")
            lines.append("")
            lines.append("Package list: " + ", ".join(f"`{r['package_name']}`" for r in records))
        lines.append("")

    OUTPUT.write_text("\n".join(lines), encoding="utf-8")
    print(f"[generate-provenance] wrote {OUTPUT}")


if __name__ == "__main__":
    main()
